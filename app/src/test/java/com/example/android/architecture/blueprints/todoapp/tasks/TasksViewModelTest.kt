        val task2 = Task("Title2", "Description2")
        val task3 = Task("Title3", "Description3")
        tasksRepository.addTasks(task1, task2, task3)
        // Given a fresh TasksViewModel
        tasksViewModel = TasksViewModel(tasksRepository)
    }

